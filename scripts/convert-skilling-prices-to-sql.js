/**
 * Converts default-skilling-prices.json to SQL INSERT statements.
 * Run: node scripts/convert-skilling-prices-to-sql.js
 * Output: supabase/migrations/00051_seed_osrs_skilling_prices.sql
 */

const fs = require("fs");
const path = require("path");

const SKILL_NAME_TO_SLUG = {
  Agility: "agility",
  Attack: "attack",
  Construction: "construction",
  Cooking: "cooking",
  Crafting: "crafting",
  Defence: "defence",
  Farming: "farming",
  Firemaking: "firemaking",
  Fishing: "fishing",
  Fletching: "fletching",
  Herblore: "herblore",
  Hunter: "hunter",
  Magic: "magic",
  Prayer: "prayer",
  Ranged: "ranged",
  Runecrafting: "runecrafting",
  Sailing: "sailing",
  Slayer: "slayer",
  Smithing: "smithing",
  Strength: "strength",
  Thieving: "thieving",
  Mining: "mining",
  Woodcutting: "woodcutting",
};

const OSRS_GAME_ID = "a8c56ade-08b9-4765-a596-9d9a3d5b1ab7";

function escapeSql(str) {
  return (str ?? "").replace(/'/g, "''");
}

const jsonPath = path.join(__dirname, "../supabase/migrations/default-skilling-prices.json");
const data = JSON.parse(fs.readFileSync(jsonPath, "utf8"));

const inserts = [];
let sortOrder = 0;

for (const skill of data) {
  const slug = SKILL_NAME_TO_SLUG[skill.skill_name] ?? skill.skill_name.toLowerCase().replace(/\s+/g, "-");
  for (const method of skill.methods || []) {
    inserts.push(
      `('${OSRS_GAME_ID}', '${escapeSql(slug)}', ${method.id}, '${escapeSql(method.method_name)}', ${method.level_min}, ${method.level_max}, ${method.gp_per_xp}, ${sortOrder++})`
    );
  }
}

const sql = `-- Seed data from default-skilling-prices.json
-- Generated by scripts/convert-skilling-prices-to-sql.js

INSERT INTO osrs_skilling_prices (game_id, skill_slug, method_id, method_name, level_min, level_max, gp_per_xp, sort_order)
VALUES\n  ` + inserts.join(",\n  ") + `
ON CONFLICT (game_id, skill_slug, method_id, level_min, level_max) DO UPDATE SET
  method_name = EXCLUDED.method_name,
  gp_per_xp = EXCLUDED.gp_per_xp,
  sort_order = EXCLUDED.sort_order,
  updated_at = NOW();
`;

const outPath = path.join(__dirname, "../supabase/migrations/00051_seed_osrs_skilling_prices.sql");
fs.writeFileSync(outPath, sql, "utf8");
console.log(`Written ${inserts.length} inserts to ${outPath}`);
console.log(`Skills: ${[...new Set(data.map((s) => SKILL_NAME_TO_SLUG[s.skill_name] ?? s.skill_name))].join(", ")}`);
